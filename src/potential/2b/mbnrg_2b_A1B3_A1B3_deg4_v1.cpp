        xs[25] = vs[25].v_exp(m_k_x_intra_B_B_1, coords_B_4_b, coords_B_5_b);
        xs[26] = vs[26].v_exp(m_k_x_intra_B_B_1, coords_B_4_b, coords_B_6_b);
